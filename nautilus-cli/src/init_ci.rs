#[cfg(test)]
mod tests {
    use super::*;
    use tempfile::TempDir;

    fn default_args(tmp: &TempDir) -> InitCiArgs {
        InitCiArgs {
            cpu_count: 2,
            memory_mib: 4096,
            dockerfile: PathBuf::from("Dockerfile"),
            output_dir: tmp.path().join(".github/workflows"),
        }
    }

    #[tokio::test]
    async fn test_init_ci_creates_file() {
        let tmp = TempDir::new().unwrap();
        let args = default_args(&tmp);
        let out_dir = args.output_dir.clone();

        run(args).await.expect("init-ci should succeed");

        let workflow = out_dir.join("nautilus-deploy.yml");
        assert!(workflow.exists(), "Expected workflow file to be created");
    }

    #[tokio::test]
    async fn test_generated_workflow_contains_required_secrets() {
        let tmp = TempDir::new().unwrap();
        let args = default_args(&tmp);
        let out_dir = args.output_dir.clone();

        run(args).await.unwrap();

        let content = std::fs::read_to_string(out_dir.join("nautilus-deploy.yml")).unwrap();
        assert!(content.contains("TEE_EC2_HOST"));
        assert!(content.contains("TEE_EC2_USER"));
        assert!(content.contains("TEE_EC2_SSH_KEY"));
        assert!(content.contains("nitro-cli run-enclave"));
        assert!(content.contains("appleboy/scp-action"));
    }

    #[tokio::test]
    async fn test_generated_workflow_uses_correct_resources() {
        let tmp = TempDir::new().unwrap();
        let args = InitCiArgs {
            cpu_count: 4,
            memory_mib: 8192,
            dockerfile: PathBuf::from("Containerfile"),
            output_dir: tmp.path().join(".github/workflows"),
        };
        let out_dir = args.output_dir.clone();

        run(args).await.unwrap();
        let content = std::fs::read_to_string(out_dir.join("nautilus-deploy.yml")).unwrap();

        assert!(content.contains("--cpu-count 4"));
        assert!(content.contains("--memory 8192"));
        assert!(content.contains("Containerfile"));
    }

    #[test]
    fn test_generate_workflow_is_valid_yaml_shape() {
        let args = InitCiArgs {
            cpu_count: 2,
            memory_mib: 4096,
            dockerfile: PathBuf::from("Dockerfile"),
            output_dir: PathBuf::from(".github/workflows"),
        };
        let yaml = generate_workflow(&args);
        // Basic sanity: YAML must start with a comment or key
        assert!(yaml.trim_start().starts_with('#') || yaml.trim_start().starts_with("name:"));
        assert!(yaml.contains("on:"));
        assert!(yaml.contains("jobs:"));
    }
}
