#[cfg(test)]
mod tests {
    use super::*;
    use tempfile::TempDir;

    fn default_args(tmp: &TempDir) -> InitCiArgs {
        InitCiArgs {
            cpu_count: 2,
            memory_mib: 4096,
            dockerfile: PathBuf::from("Dockerfile"),
            output_dir: tmp.path().join(".github/workflows"),
        }
    }

    #[test]
    fn test_generate_rust_workflow_contains_required_secrets() {
        let args = default_args(&TempDir::new().unwrap());
        let content = generate_workflow_rust(&args);
        assert!(content.contains("TEE_EC2_HOST"));
        assert!(content.contains("TEE_EC2_USER"));
        assert!(content.contains("TEE_EC2_SSH_KEY"));
        assert!(content.contains("nitro-cli run-enclave"));
        assert!(content.contains("appleboy/scp-action"));
        assert!(content.contains("socat"));
    }

    #[test]
    fn test_generate_rust_workflow_uses_correct_resources() {
        let args = InitCiArgs {
            cpu_count: 4,
            memory_mib: 8192,
            dockerfile: PathBuf::from("Containerfile"),
            output_dir: PathBuf::from(".github/workflows"),
        };
        let content = generate_workflow_rust(&args);
        assert!(content.contains("--cpu-count 4"));
        assert!(content.contains("--memory 8192"));
        assert!(content.contains("Containerfile"));
    }

    #[test]
    fn test_generate_rust_workflow_is_valid_yaml_shape() {
        let args = default_args(&TempDir::new().unwrap());
        let yaml = generate_workflow_rust(&args);
        assert!(yaml.trim_start().starts_with('#') || yaml.trim_start().starts_with("name:"));
        assert!(yaml.contains("on:"));
        assert!(yaml.contains("jobs:"));
    }

    #[test]
    fn test_generate_ts_workflow_uses_argonaut() {
        let args = default_args(&TempDir::new().unwrap());
        let content = generate_workflow_ts(&args);
        assert!(content.contains("argonaut"));
        assert!(content.contains("make"));
        assert!(content.contains("nautilus-argonaut.service"));
        assert!(!content.contains("socat"));
    }

    #[test]
    fn test_generate_ts_workflow_copies_correct_sources() {
        let args = default_args(&TempDir::new().unwrap());
        let content = generate_workflow_ts(&args);
        assert!(content.contains("package.json"));
        assert!(content.contains("bun.lock"));
        assert!(content.contains("tsconfig.json"));
    }

    #[test]
    fn test_generate_ts_workflow_is_valid_yaml_shape() {
        let args = default_args(&TempDir::new().unwrap());
        let yaml = generate_workflow_ts(&args);
        assert!(yaml.trim_start().starts_with('#') || yaml.trim_start().starts_with("name:"));
        assert!(yaml.contains("on:"));
        assert!(yaml.contains("jobs:"));
    }
}
